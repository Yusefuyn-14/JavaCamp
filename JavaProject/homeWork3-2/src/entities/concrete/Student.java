package entities.concrete;

import entities.abstracts.user;

public class Student extends user {
	public String[] completedCamps;
	public String[] dontCompletedCamps;
	
	public Student() {
		super();
	}

	public Student(String[] completedCamps, String[] dontCompletedCamps) {
		super();
		this.completedCamps = completedCamps;
		this.dontCompletedCamps = dontCompletedCamps;
	}

	public String[] getCompletedCamps() {
		return completedCamps;
	}

	public void setCompletedCamps(String[] completedCamps) {
		this.completedCamps = completedCamps;
	}

	public String[] getDontCompletedCamps() {
		return dontCompletedCamps;
	}

	public void setDontCompletedCamps(String[] dontCompletedCamps) {
		this.dontCompletedCamps = dontCompletedCamps;
	}
}
